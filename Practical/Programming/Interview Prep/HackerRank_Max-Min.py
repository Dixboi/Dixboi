def maxMin(k, arr):
    arr.sort()
    
    uf_min = 10000000000
    
    for _ in range(len(arr)-k+1):
        uf_temp = arr[_+k-1] - arr[_]
        if uf_temp < uf_min:
            uf_min = uf_temp
        
    return uf_min

