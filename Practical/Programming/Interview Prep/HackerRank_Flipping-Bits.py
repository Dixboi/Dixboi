def flippingBits(n):
    binary = str(bin(n)[2:]).zfill(32).replace('0', '_').replace('1', '0').replace('_', '1')
    return int(binary, 2)
    

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    q = int(input().strip())

    for q_itr in range(q):
        n = int(input().strip())

        result = flippingBits(n)

        fptr.write(str(result) + '\n')

    fptr.close()
