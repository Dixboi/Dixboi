def pangrams(s):
    s = s.lower().replace(" ", "")
    
    letters = [0] * 26
    
    for _ in s:
        letters[ord(_) - ord("a")] += 1
    
    return "pangram" if letters.count(0) == 0 else "not pangram"

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    s = input()

    result = pangrams(s)

    fptr.write(result + '\n')

    fptr.close()
